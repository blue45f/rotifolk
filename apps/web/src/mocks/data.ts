const now = new Date()
const minutesAgo = (m: number) => new Date(now.getTime() - m * 60_000).toISOString()
const inDays = (d: number, h = 19) => {
  const t = new Date(now)
  t.setDate(t.getDate() + d)
  t.setHours(h, 30, 0, 0)
  return t.toISOString()
}

